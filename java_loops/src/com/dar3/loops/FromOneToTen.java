package com.dar3.loops;

public class FromOneToTen {

//Tasks were taken from http://www.beginwithjava.com/java/loops/questions.html
//Task: Write a program to print numbers from 1 to 10.

    public static void main(String[] args){

        for(int i = 1; i <= 10; i++){

            System.out.println(i);

        }

    }
}
